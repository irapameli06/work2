public class CompareNumbers {
    public static void main(String[] args) {
       
        int a = 12;
        int b = 43;

        if (a > b) {
            System.out.println(a + " is larger.");
        } else if (b > a) {
            System.out.println(b + " is larger.");
        } else {
            System.out.println("Both numbers are equal.");
        }
    }
}
